class ArithmeticOperators {
  public static void main(String[] args) {

    int a = 10 + 5;
    double b = 4.5 * 2;
    double c = b + a;
    double d = 4.0 / 3.0;
    double e = 4.0 % 3.0;
    
    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
  }
}
