class VariableConventions {
  public static void main(String[] args) {
    
    int age;
    int $age; // não recomendado
    int _age; // não recomendado
    int age2;
    int Age;  // não recomendado
    int AGE;  // não recomendado
    
    String firstName; // OK!
    String lastName;  // OK!
    
    int string;  // OK!
    int String;  // não recomendado
  }
}
